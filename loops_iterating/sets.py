my_set = {1000, 2000, 3000, 4000, 5000}
for member in my_set:
    print(member)

my_dict = {'a': 1, 'b': 2, 'c': 3}
for key in my_dict:
    print(key)

for value in my_dict.values():
    print(value)

for item in my_dict.items():
    print(item)

for key, value in my_dict.items():
    print(f'{key} = {value}')
